package com.clint.animals.exercise.animals.exercise.model;

public class Animal {

    private int animalsCanFly;
    private int animalsCanWalk;
    private int animalsCanSing;
    private int animalsCanSwim;
    private int animalsCanJoke;

    public int getAnimalsCanFly() {
        return animalsCanFly;
    }

    public void setAnimalsCanFly(int animalsCanFly) {
        this.animalsCanFly = animalsCanFly;
    }

    public int getAnimalsCanWalk() {
        return animalsCanWalk;
    }

    public void setAnimalsCanWalk(int animalsCanWalk) {
        this.animalsCanWalk = animalsCanWalk;
    }

    public int getAnimalsCanSing() {
        return animalsCanSing;
    }

    public void setAnimalsCanSing(int animalsCanSing) {
        this.animalsCanSing = animalsCanSing;
    }

    public int getAnimalsCanSwim() {
        return animalsCanSwim;
    }

    public void setAnimalsCanSwim(int animalsCanSwim) {
        this.animalsCanSwim = animalsCanSwim;
    }

    public int getAnimalsCanJoke() {
        return animalsCanJoke;
    }

    public void setAnimalsCanJoke(int animalsCanJoke) {
        this.animalsCanJoke = animalsCanJoke;
    }
}
